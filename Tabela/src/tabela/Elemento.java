package tabela;

class Elemento {
    private double valor;

    //Construtores.
    public Elemento() {
    }
    public Elemento(double valor) {
        this.valor = valor;
    }
    
    //Acessores.
    public double getValor() {
        return valor;
    }
    public void setValor(double valor) {
        this.valor = valor;
    }
    
}
